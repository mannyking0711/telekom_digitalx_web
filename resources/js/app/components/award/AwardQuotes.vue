<script>

	import QuotesSlider from "../sections/QuotesSlider";

	export default {

		/////////////////////////////////
		// INIT
		/////////////////////////////////

		name: 'awardQuotes',

		props: {
			headline: String,
			quotes: Array
		},

		components: {
			QuotesSlider
		},

		data() {
			return {

			};
		},

		computed: {
			sliderQuotes: function() {
				return this.quotes.map(quote => {
					if(quote.image.url){
						quote.image = quote.image.url;
					}
					return quote;
				});
			}
		},


		/////////////////////////////////
		// EVENTS
		/////////////////////////////////

		/////////////////////////////////
		// METHODS
		/////////////////////////////////

		methods: {

		}

	}; // end export

