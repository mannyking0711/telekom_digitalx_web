<template>
  <section class="agenda-day-selector inner no-spacing">
    <div class="col-100">
      <div class="agenda-day-selector__label">
        {{ $t('agenda-day-selector.label') }}
      </div>

      <div class="agenda-day-selector__buttons">
        <button
          v-for="(day, index) in agenda.days"
          :key="day.id"
          :class="{ active: activeDayIndex === index }"
          class="btn blue agenda-day-selector__button"
          @click="setActiveDayIndex(index)"
        >
          {{ day.title }}
        </button>
      </div>
    </div>
  </section>
</template>


<!--/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    JAVASCRIPT
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


<script>
export default {
  /////////////////////////////////
  // INIT
  /////////////////////////////////

  name: "AgendaDaySelector",

  props: {
    agenda: Object,
  },

  data() {
    return {
      activeDayIndex: undefined,
    };
  },

  computed: {},

  watch: {
    agenda: {
      immediate: true,
      handler: function () {
        if (this.agenda && this.agenda.days && this.agenda.days.length) {
          // Set first day as default index
          this.setActiveDayIndex(0);
        }
      },
    },

    activeDayIndex: {
      immediate: true,
      handler: function (value) {
        this.$emit("set-day", value);
      },
    },
  },

  /////////////////////////////////
  // EVENTS
  /////////////////////////////////

  created() {
  },

  mounted() {
  },

  /////////////////////////////////
  // METHODS
  /////////////////////////////////

  methods: {
    setActiveDayIndex: function (index) {
      this.activeDayIndex = index;
    },
  },
}; // end export
</script>


