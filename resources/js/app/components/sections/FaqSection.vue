<template>
	<div>
		<div v-for="group in filteredGroups" :key="group.id" class="accordeon-group">
			<h3 class="accordeon-group-headline">{{ group.name }}</h3>
			<div v-for="faq in group.faqs" :key="faq.id">
				<Accordeon :header="faq.question" icon="app/icon-arrow">
					<div v-html="faq.answer"></div>
				</Accordeon>
			</div>
		</div>
	</div>
</template>



<!--/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    JAVASCRIPT
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


<script>

	import fetchItemMixin from '../../mixins/fetchItem.js';
	import Accordeon from "../../../components/accordeon/Accordeon";

	export default {

		/////////////////////////////////
		// INIT
		/////////////////////////////////

		name: 'FaqSection',

		mixins: [fetchItemMixin],

		props: {
			// only show FAQs regarding the participation
			participate: { type: Boolean, default: false }
		},

		components: {
			Accordeon
		},

		data() {
			return {
				ENDPOINT: 'faq-groups/'
			};
		},

		computed: {
			groups() {
				return this.itemData && this.itemData.length
					? this.itemData.filter(group => group.faqs.length)
					: [];
			},
			filteredGroups() {
				return this.participate ? this.groups.filter(group => group.participate) : this.groups;
			}
		},


		/////////////////////////////////
		// EVENTS
		/////////////////////////////////

		created() {},

		mounted() {},


		/////////////////////////////////
		// METHODS
		/////////////////////////////////

		methods: {},

	}; // end export

</script>



