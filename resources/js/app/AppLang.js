	// Vue framework
	import Vue from 'vue'
	import VueI18n from 'vue-i18n'

	// Messages
	import messages from '../lang/LangGlobal.js'
	import messagesApp from '../lang/LangApp.js'



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	I18N
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */

    Vue.use(VueI18n);

    export default function createI18n(store) {

        // combine global and app language
        let allMessages = {};
        for (let lang in messagesApp) {	allMessages[lang] = Object.assign(messages[lang],messagesApp[lang]); }

        // expose i18n
        return new VueI18n({
            locale: 				store.getters.env.active_locale,
            fallbackLocale: 		store.getters.env.fallback_locale,
            silentTranslationWarn: 	true,
            messages: 				allMessages
        });

    }
