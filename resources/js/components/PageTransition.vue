<!--/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    HTML
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


	<template>
		<transition :name="transitionName" :mode="transitionMode">
			<slot></slot>
		</transition>
	</template>



<!--/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    JAVASCRIPT
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


	<script>

		import {IS_SSR} from "../app/plugins/ssrSwitch";

    export default {

			/////////////////////////////////
			// INIT
			/////////////////////////////////

			name: 'PageTransition',

			data() {
				return {
					transitionName: 'page-fade',
					transitionMode: 'out-in',
				};
			},

			computed: {

			},


			/////////////////////////////////
			// EVENTS
			/////////////////////////////////

			created() {
        if (IS_SSR === true) {
          return;
        }

				this.$router.beforeEach((to, from, next) => {

					// set type of transition
					this.transitionName = to.meta.transition || from.meta.transition || 'page-fade';
					this.transitionMode = 'out-in';

					next();
				});
			},


			/////////////////////////////////
			// METHODS
			/////////////////////////////////

			methods: {

				beforeLeave(element) 	{ },
				enter(element) 			{ },
				afterEnter(element) 	{ },
			},


		}; // end export

	</script>



