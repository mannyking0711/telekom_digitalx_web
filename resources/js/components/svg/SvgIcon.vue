<!--/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    HTML
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


	<template>

		<!-- INCLUDE DATA -->
		<inline-svg class="svg-inline" v-if="path" :src="filePath" @loaded="$emit('loaded')"></inline-svg>

		<!-- USE ICON SPRITE -->
		<svg v-else-if="icon" class="icon">
			<use :xlink:href="iconSprite" />
		</svg>

		<!--- USE SPRITE -->
		<svg v-else class="sprite">
			<use :xlink:href="fileSprite" />
		</svg>

	</template>



<!--/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    JAVASCRIPT
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


	<script>

		import InlineSvg from 'vue-inline-svg';

		export default {

			/////////////////////////////////
			// INIT
			/////////////////////////////////

			name: 'SvgIcon',

			components: { InlineSvg },

			props: {
				path: 		{type: String },			// use a single svg file. "app/logo"
				sprite: 	{type: String },			// use a symbol from svg sprite. "app/icon-close"
				icon: 		{type: String },			// use a symbol from svg icons sprite. "app/icon-close"
			},

			data() {
				return {

				};
			},

			computed: {

				basePath() 		{ return this.$store.getters.env.base_url + 'svg/'; },
				filePath() 		{ return this.basePath + this.path + '.svg'; },
				iconSprite() 	{ return this.basePath + this.icon.replace('/','/sprite-icons.svg#sprite-'); },
				fileSprite() 	{ return this.basePath + this.sprite.replace('/','/sprite.svg#sprite-'); },
			},


			/////////////////////////////////
			// EVENTS
			/////////////////////////////////

			created() {

			},

			mounted() {

			},


			/////////////////////////////////
			// METHODS
			/////////////////////////////////

			methods: {

			}

		}; // end export

	</script>



