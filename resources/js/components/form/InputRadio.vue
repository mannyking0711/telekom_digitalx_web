//    HTML
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


	<template>

		<div :class="['form-row', id ? 'row-'+id : '']">
			<label :class="{error:error}">{{ $t(label) }}</label>
			<div v-for="(item,index) in items" :class="['input-radio',{'error':error}]">
				<input
					:id="id+'-'+index"
					:name="id"
					type="radio"
					:value="getItemValue(item)"
					v-model="value"
					@change="updateInput"
				>
				<label :for="id+'-'+index">{{ $t(getItemLabel(item)) }}</label>
				<svg-icon path="app/icon-radio"></svg-icon>
			</div>

		</div>

	</template>



<!--/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    JAVASCRIPT
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


	<script>

		export default {

			/////////////////////////////////
			// INIT
			/////////////////////////////////

			name: 'InputRadio',

			model: {
				prop: 'propValue',
				event: 'change'
			},

			props: {
				label: 		{ type: String },
				id: 		{ type: String, required: true },
				propValue:  { default: undefined },
				items: 		{ type: Array, default() { return []; } },
				error: 		undefined
			},

			data() {
				return {
					value: ""
				};
			},

			computed: {

			},


			/////////////////////////////////
			// EVENTS
			/////////////////////////////////

			created() {

			},

			mounted() {

				this.value = this.propValue;
			},


			/////////////////////////////////
			// METHODS
			/////////////////////////////////

			methods: {

				getItemValue(item) {

					return Object.values(item)[0];
				},

				getItemLabel(item) {

					return Object.keys(item)[0];
				},

				updateInput(e) {

					// update parent model binding
					this.$emit('change',this.value);
				}
			}

		}; // end export

	</script>



