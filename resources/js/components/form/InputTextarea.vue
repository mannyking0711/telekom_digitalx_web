<!--/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    HTML
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


	<template>

		<div :class="['form-row', id ? 'row-'+id : '']">
			<label v-if="label" :class="{error:error}">{{ $t(label) }}</label>
			<textarea
				:id="id"
				:name="id"
				v-model="value"
				:class="{error:showError}"
                :placeholder="placeholder"
				@input="updateInput"
				@focus="removeError"
			></textarea>
		</div>

	</template>



<!--/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    JAVASCRIPT
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


	<script>

		export default {

			/////////////////////////////////
			// INIT
			/////////////////////////////////

			name: 'InputTextarea',

			model: {
				prop: 'propValue',
				event: 'change'
			},

			props: {
				label: 		    { type: String },
				id: 		    { type: String },
				readonly: 	    { type: Boolean },
				propValue:      { type: String },
				error: 		    undefined,
                placeholder:    { type: String },
			},

			data() {
				return {
					value: "",
					showError: false,
				};
			},

			computed: {

			},


			/////////////////////////////////
			// EVENTS
			/////////////////////////////////

			created() {

			},

			mounted() {

				this.value = this.propValue;
				this.showError = this.error;
			},


			/////////////////////////////////
			// METHODS
			/////////////////////////////////

			methods: {

				updateInput(e) {

					// update parent model binding
					this.$emit('change',e.target.value);
				},

				removeError(e) {
					this.showError = false;
				}
			},

            watch: {

                error(newValue) {
                    this.showError = newValue;
                }
            }

		}; // end export

	</script>



