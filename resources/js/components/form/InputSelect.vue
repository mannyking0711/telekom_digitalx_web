	<template>

		<div :class="['form-row', id ? 'row-'+id : '']">

			<label v-if="label" :class="{error:error}">{{ $t(label) }}</label>
			<div :class="['input-select',{'error':showError}]">
				<svg-icon icon="app/icon-select"></svg-icon>
				<select
					:id="id"
					:name="id"
					v-model="value"
					@input="updateInput"
					@focus="removeError"
				>
					<option value="">-- {{ $t('Bitte ausw√§hlen') }} --</option>
					<option v-for="item in items" :value="typeof item != 'object' ? item : Object.values(item)[0] ">{{ $t(typeof item != 'object' ? item : Object.keys(item)[0]) }}</option>
				</select>
			</div>

		</div>

	</template>



<!--/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    JAVASCRIPT
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


	<script>

		export default {

			/////////////////////////////////
			// INIT
			/////////////////////////////////

			name: 'InputSelect',

			model: {
				prop: 'propValue',
				event: 'change'
			},

			props: {
				label: 		{ type: String },
				id: 		{ type: String },
				propValue:  { type: String, default: undefined },
				items: 		{ type: Array, default() { return []; } },
				error: 		undefined
			},

			data() {
				return {
					value: "",
					showError: false,
				};
			},

			computed: {

			},


			/////////////////////////////////
			// EVENTS
			/////////////////////////////////

			created() {

			},

			mounted() {

				this.value = this.propValue;
				this.showError = this.error;
			},


			/////////////////////////////////
			// METHODS
			/////////////////////////////////

			methods: {

				updateInput(e) {

					// update parent model binding
					this.$emit('change',e.target.value);
				},

				removeError(e) {

					this.showError = false;
				}
			}

		}; // end export

	</script>



